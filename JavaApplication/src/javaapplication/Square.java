public class Square {
    private int x;
	private int y;
	private String s;
	public void Square (){

	};
	public void set(int x,int y,String s){
		this.x = x;
		this.y = y;
		this.s = s;
	}
	public int getX()
	{
		int x = this.x;
		return x;
	}
	public int getY()
	{
		int y = this.y;
		return y;
	}
	public String getS()
	{
		String s = this.s;
		return s;
	}
  
}
